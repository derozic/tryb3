/// TUX Design System - Spacing Constants
///
/// Provides consistent spacing values across the application.
/// Based on an 8px grid system for optimal design harmony.
class TuxSpacing {
  const TuxSpacing._();

  /// Extra small spacing: 4px
  static const double xs = 4.0;

  /// Small spacing: 8px
  static const double sm = 8.0;

  /// Medium spacing: 16px
  static const double md = 16.0;

  /// Large spacing: 24px
  static const double lg = 24.0;

  /// Extra large spacing: 32px
  static const double xl = 32.0;

  /// Extra extra large spacing: 40px
  static const double xxl = 40.0;

  /// Extra extra extra large spacing: 48px
  static const double xxxl = 48.0;
}